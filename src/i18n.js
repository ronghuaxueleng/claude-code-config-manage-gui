// 多语言管理类
class I18n {
    constructor() {
        // 默认语言为中文
        this.currentLanguage = localStorage.getItem('language') || 'zh-CN';
        this.translations = translations;
    }

    // 获取当前语言
    getLanguage() {
        return this.currentLanguage;
    }

    // 切换语言
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem('language', lang);
            this.updatePageText();
            // 触发语言切换事件
            window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
            return true;
        }
        return false;
    }

    // 获取翻译文本
    t(key) {
        // 直接从当前语言的翻译对象中获取翻译值
        const translation = this.translations[this.currentLanguage][key];
        // 如果没有找到翻译，返回 key
        return translation !== undefined ? translation : key;
    }

    // 更新页面所有带有 data-i18n 属性的元素
    updatePageText() {
        // 更新标题
        document.title = this.t('app.title');

        // 更新所有带 data-i18n 属性的元素
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (element.tagName === 'INPUT' && element.getAttribute('placeholder') !== null) {
                element.placeholder = this.t(key);
            } else {
                const icon = element.querySelector('i');
                const iconHTML = icon ? icon.outerHTML + ' ' : '';
                element.innerHTML = iconHTML + this.t(key);
            }
        });

        // 更新所有带 data-i18n-placeholder 属性的元素
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            element.placeholder = this.t(key);
        });

        // 更新所有带 data-i18n-title 属性的元素
        document.querySelectorAll('[data-i18n-title]').forEach(element => {
            const key = element.getAttribute('data-i18n-title');
            element.title = this.t(key);
        });
    }
}

// 创建全局实例
const i18n = new I18n();

// 页面加载完成后初始化
document.addEventListener('DOMContentLoaded', () => {
    i18n.updatePageText();
});

// 导出供其他模块使用
window.i18n = i18n;
